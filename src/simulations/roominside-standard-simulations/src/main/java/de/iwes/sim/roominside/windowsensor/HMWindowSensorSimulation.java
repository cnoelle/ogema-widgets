package de.iwes.sim.roominside.windowsensor;

import java.util.HashMap;
import java.util.List;
import java.util.Map;

import org.ogema.apps.roomsim.service.api.RoomSimulationService;
import org.ogema.apps.roomsim.service.api.helpers.RoomConnection;
import org.ogema.apps.roomsim.service.api.helpers.RoomInsideLogicBase;
import org.ogema.apps.roomsim.service.api.helpers.RoomInsideSimBase;
import org.ogema.core.application.ApplicationManager;
import org.ogema.core.application.Timer;
import org.ogema.core.model.Resource;
import org.ogema.model.devices.sensoractordevices.SensorDevice;
import org.ogema.model.sensors.DoorWindowSensor;
import org.ogema.tools.simulation.service.api.model.SimulatedQuantity;
import org.ogema.tools.simulation.service.api.model.SimulationConfiguration;

import de.iwes.util.resource.ValueResourceHelper;

/**
 * A simulation provider, that simulates e.g. a particular sort of devices. 
 * In this example, a the power generated by a PV plant is simulated.  
 */
public class HMWindowSensorSimulation extends RoomInsideSimBase<HMWindowSensorConfigPattern, HMWindowSensorPattern> {
	
	private static final long SIM_UPDATE_INTERVAL = 4000;
	@Override
	protected long getDefaultUpdateInterval(){return SIM_UPDATE_INTERVAL;}

	// callbacks are guaranteed to come in the same thread, no need to synchronize on the map
	private final Map<String,SimulatedWindowSens> simulatedObjects = new HashMap<>();
	private final RoomSimulationService roomSimService;
	
	public HMWindowSensorSimulation(ApplicationManager am, RoomSimulationService roomSimService) {
		super(am, HMWindowSensorPattern.class, false, HMWindowSensorConfigPattern.class);
		this.roomSimService = roomSimService;
	}	

	@Override
	public String getProviderId() {
		return "Window-Door Sensor simulation _Room Simulation_";
	}
	
	@Override
	public Class<? extends Resource> getSimulatedType() {
		return DoorWindowSensor.class;
	}

	@Override
	public void buildConfigurations(HMWindowSensorPattern pattern, List<SimulationConfiguration> cfgs, HMWindowSensorConfigPattern simPattern) {
		// Add here configuration values that can be edited by the user, see example below
		cfgs.add(new RoomConnection<>(simPattern.roomSimulationToConnect, appManager.getResourceAccess(),
				roomSimService, this, simPattern));
		cfgs.add(new ThermalLossWhenOpenConfig(simPattern.thermalLossWhenOpen));
		cfgs.add(new BooleanSensorState(pattern.open));
	}
	
	@Override
	public void buildQuantities(HMWindowSensorPattern pattern, List<SimulatedQuantity> quantities, HMWindowSensorConfigPattern simPattern) {
		// Add here configuration values that can be edited by the user, see example below
	}
	
	@Override
	public String getDescription() {
		return "Simulated window sensor connected into room via Room Simulation Service."
				+ " Note that after creation of a simulated object the room connector has to be set before"
				+ "the simulation can be activated.";
	}
	
	@Override
	public void simTimerElapsed(HMWindowSensorPattern targetPattern, HMWindowSensorConfigPattern configPattern, Timer t, long timeStep) {
		//here the timing is provided by the room simulations service directly to the component
	}

	
	@SuppressWarnings("unchecked")
	@Override
	protected void initSimulation(HMWindowSensorPattern targetPattern, HMWindowSensorConfigPattern configPattern) {
		// initialize and activate also the optional fields in targetPattern
		boolean finalActivate = false;
		if(ValueResourceHelper.setIfNew(targetPattern.open, false) |
				ValueResourceHelper.setIfNew(targetPattern.batteryVoltage, 2.6f) |
				ValueResourceHelper.setIfNew(targetPattern.elStorageType, 1) |
				ValueResourceHelper.setIfNew(targetPattern.batteryStatus, 0.8f)) {
			finalActivate = true;
		}
		if(finalActivate) {
			targetPattern.model.activate(true);
		}
		if(ValueResourceHelper.setIfNew(configPattern.thermalLossWhenOpen, 100)) {
			configPattern.model.activate(true);
		}

		SimulatedWindowSens logic = new SimulatedWindowSens(appManager, targetPattern, configPattern,
				roomSimService, this);
		simulatedObjects.put(targetPattern.model.getLocation(), logic);
	}
	
	@Override
	protected void removeSimulation(HMWindowSensorPattern targetPattern, HMWindowSensorConfigPattern configPattern) {
		SimulatedWindowSens logic = simulatedObjects.remove(targetPattern.model.getLocation());
		logic.close();
	}

	@Override
	protected RoomInsideLogicBase<?> getSimulatedObjects(String simLocation) {
		return simulatedObjects.get(simLocation);
	}
}
