    private static void make(String input) {
        String[] array = input.replace("\n", "").split(",");
        final String glyphicon = "glyphicon glyphicon-";
        for (String s : array) {
            String upper = s.toUpperCase().replace("-", "_");
            System.out.println("public static final String " + upper + " = \"" + glyphicon + s + "\";");
        }
    }

